def solve_part1(lines):
    # Split into map and moves
    map_lines = []
    moves = []
    parsing_moves = False
    
    for line in lines:
        if line.strip() == "":
            parsing_moves = True
            continue
        if parsing_moves:
            moves.extend(list(line.strip()))
        else:
            map_lines.append(line.strip())
    
    # Find robot position
    robot_r, robot_c = None, None
    for r, row in enumerate(map_lines):
        for c, ch in enumerate(row):
            if ch == '@':
                robot_r, robot_c = r, c
                break
        if robot_r is not None:
            break
    
    # Process moves
    drs = {'^': -1, 'v': 1, '<': 0, '>': 0}
    dcs = {'^': 0, 'v': 0, '<': -1, '>': 1}
    
    for move in moves:
        nr = robot_r + drs[move]
        nc = robot_c + dcs[move]
        
        # Check if next cell is wall
        if map_lines[nr][nc] == '#':
            continue
            
        # If next cell is empty, just move
        if map_lines[nr][nc] == '.':
            map_lines[robot_r] = map_lines[robot_r][:robot_c] + '.' + map_lines[robot_r][robot_c+1:]
            map_lines[nr] = map_lines[nr][:nc] + '@' + map_lines[nr][nc+1:]
            robot_r, robot_c = nr, nc
            continue
            
        # If next cell is box, try to push
        if map_lines[nr][nc] == 'O':
            # Find where the push chain ends
            push_r, push_c = nr, nc
            can_push = True
            
            while True:
                next_push_r = push_r + drs[move]
                next_push_c = push_c + dcs[move]
                
                if map_lines[next_push_r][next_push_c] == '#':
                    can_push = False
                    break
                if map_lines[next_push_r][next_push_c] == '.':
                    break
                    
                push_r, push_c = next_push_r, next_push_c
            
            if can_push:
                # Move robot
                map_lines[robot_r] = map_lines[robot_r][:robot_c] + '.' + map_lines[robot_r][robot_c+1:]
                map_lines[nr] = map_lines[nr][:nc] + '@' + map_lines[nr][nc+1:]
                
                # Move boxes in chain
                while True:
                    next_push_r = push_r + drs[move]
                    next_push_c = push_c + dcs[move]
                    
                    if map_lines[next_push_r][next_push_c] == '.':
                        map_lines[push_r] = map_lines[push_r][:push_c] + '.' + map_lines[push_r][push_c+1:]
                        map_lines[next_push_r] = map_lines[next_push_r][:next_push_c] + 'O' + map_lines[next_push_r][next_push_c+1:]
                        break
                    
                    map_lines[push_r] = map_lines[push_r][:push_c] + '.' + map_lines[push_r][push_c+1:]
                    map_lines[next_push_r] = map_lines[next_push_r][:next_push_c] + 'O' + map_lines[next_push_r][next_push_c+1:]
                    push_r, push_c = next_push_r, next_push_c
                
                robot_r, robot_c = nr, nc
    
    # Calculate GPS coordinates
    total = 0
    for r, row in enumerate(map_lines):
        for c, ch in enumerate(row):
            if ch == 'O':
                total += 100 * r + c
    
    return total

