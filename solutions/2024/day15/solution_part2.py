def solve_part2(lines):
    # Parse the map and moves
    map_lines = []
    moves = []
    parsing_map = True
    
    for line in lines:
        if line == "":
            parsing_map = False
            continue
        if parsing_map:
            map_lines.append(line)
        else:
            moves.extend(list(line))
    
    # Build the expanded map (twice as wide)
    expanded_map = []
    for row in map_lines:
        new_row = ""
        for ch in row:
            if ch == "#":
                new_row += "##"
            elif ch == "O":
                new_row += "[]"
            elif ch == ".":
                new_row += ".."
            elif ch == "@":
                new_row += "@."
        expanded_map.append(list(new_row))
    
    # Find robot position
    robot_r, robot_c = -1, -1
    for r, row in enumerate(expanded_map):
        for c, ch in enumerate(row):
            if ch == "@":
                robot_r, robot_c = r, c
                break
        if robot_r != -1:
            break
    
    # Process moves
    for move in moves:
        dr, dc = 0, 0
        if move == "^":
            dr = -1
        elif move == "v":
            dr = 1
        elif move == "<":
            dc = -1
        elif move == ">":
            dc = 1
        
        # Check what's in the way
        next_r, next_c = robot_r + dr, robot_c + dc
        if expanded_map[next_r][next_c] == "#":
            # Wall - can't move
            continue
        
        if expanded_map[next_r][next_c] == ".":
            # Empty space - move robot
            expanded_map[robot_r][robot_c] = "."
            expanded_map[next_r][next_c] = "@"
            robot_r, robot_c = next_r, next_c
            continue
        
        if expanded_map[next_r][next_c] in ["[", "]"]:
            # Box - need to check if it can be pushed
            if can_push_box(expanded_map, next_r, next_c, dr, dc):
                push_box(expanded_map, next_r, next_c, dr, dc)
                # Move robot
                expanded_map[robot_r][robot_c] = "."
                expanded_map[next_r][next_c] = "@"
                robot_r, robot_c = next_r, next_c
    
    # Calculate GPS coordinates
    total_gps = 0
    for r, row in enumerate(expanded_map):
        for c, ch in enumerate(row):
            if ch == "[":
                # This is the left half of a box
                total_gps += 100 * r + c
    
    return total_gps

